class BookData {
  final String? bookName;
  final String? author;
  final String? publisher;
  final String? yearsOfBook;
  final String? isbn;
  final String? numberOfBooks;
  final String? racks;
  final String? docId;
  BookData({
    required this.bookName,
    required this.author,
    required this.publisher,
    required this.yearsOfBook,
    required this.isbn,
    required this.numberOfBooks,
    required this.racks,
    required this.docId,
  });
}
